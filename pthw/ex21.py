# math

def add(a, b):
    print ("ADDING %d + %d" % (a, b))
    return a + b

def sub(a, b):
    print ("SUBTRACTING %d - %d" % (a, b))
    return a - b

def mult(a, b):
    print ("MULTIPLYING %d x %d" % (a, b))
    return a * b

def div(a, b):
    print ("DIVIDING %d / %d" % (a, b))
    return a / b

print ("Math fun")

res = add(1, 2)
print (res)

res = sub(1, 2)
print (res)

res = mult(1, 2)
print (res)

res = div(1, 2)
print (res)
